namespace Aliencube.AzureFunctions.Extensions.Common
{
    /// <summary>
    /// This specifies the content types.
    /// </summary>
    public static class ContentTypes
    {
        /// <summary>
        /// Identifies text/plain.
        /// </summary>
        public const string PlainText = "text/plain";

        /// <summary>
        /// Identifies text/html.
        /// </summary>
        public const string TextHtml = "text/html";

        /// <summary>
        /// Identifies application/json.
        /// </summary>
        public const string ApplicationJson = "application/json";

        /// <summary>
        /// Identifies text/vnd.yaml.
        /// </summary>
        public const string TextVndYaml = "text/vnd.yaml";

        /// <summary>
        /// Identifies application/x-www-form-urlencoded.
        /// </summary>
        public const string ApplicationFormUrlEncoded = "application/x-www-form-urlencoded";

        /// <summary>
        /// Identifies multipart/form-data.
        /// </summary>
        public const string MultipartFormData = "multipart/form-data";
    }
}
